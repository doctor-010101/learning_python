# In this example:

# name: is the key and "Hasan" is the value.

# age: is an integer.

# isStudent: is a boolean value (true or false).

# courses: is an array of strings.

# address: is a nested object containing other keys like "city" and "postalCode."


# JSON files are useful for data exchange between web services or for storing configuration information and simple data.