# A JSON file (JavaScript Object Notation) is a lightweight, text-based format for data exchange that is widely used in web programming and other programming languages. JSON uses a structure based on objects and arrays, similar to how objects are defined in JavaScript.

# Key features of JSON:

# Human-readable: It is easily readable by humans.

# Widely compatible: It is supported by almost all programming languages.

# Simple structure: It consists of two main data types:

# 1. Object: A collection of key-value pairs enclosed in {}.


# 2. Array: A list of values enclosed in [].




