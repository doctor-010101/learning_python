# Definition: 
# Interactive mode in Python allows users to execute Python commands and see immediate results, providing an interactive environment for experimentation and debugging.
# Detailed Explanation: When you start Python from the command line without a script, you enter interactive mode, where you can type Python statements and get immediate feedback. This is useful for testing small code snippets, exploring modules, or debugging.

# Example:

# # In interactive mode
# >>> x = 5
# >>> y = 10
# >>> z = x + y
# >>> print(z)
# 15

# Explanation of the example: In this interaction, the user assigns values to x and y, then calculates their sum and prints it. Each command is executed immediately, providing instant feedback, which is ideal for quick calculations or testing functions.
